let onemodal = document.getElementById('oneModal');
let onebtn = document.getElementById('button_order1');
let onespan = document.getElementsByClassName("oneclose")[0];

onebtn.onclick = function () {
    onemodal.style.display = "block";
};

onespan.onclick = function () {
    onemodal.style.display = "none";
};

window.onclick = function (event) {
    if (event.target == onemodal) {
        onemodal.style.display = "none";
    }
};

